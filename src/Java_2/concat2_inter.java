package Java_2;

public class concat2_inter {
    public static void main(String[] args) {
        String first_name = "Neha";
        String second_name = "Kumari";

        int a = 10;
        int b = 10;

        System.out.println(first_name+second_name+a+b);
        System.out.println(first_name+second_name+(a+b));
        System.out.println(a+b+first_name+second_name);

    }
}
