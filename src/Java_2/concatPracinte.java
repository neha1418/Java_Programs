package Java_2;

public class concatPracinte {
    public static void main(String[] args) {
        String s1 = "Neha";
        String s2 = "Kumari";
        System.out.println("s1+s2");

        int a = 10;
        int b = 20;
        System.out.println(a+b);

        //+ behave differently with the data type which is called Operator Overloading
    }

}
